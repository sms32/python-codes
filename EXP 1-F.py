#AIM :PYTHON CODE TO CHECK ARMSTRONG OR NOT
"""
STEP 1: START
STEP 2: ENTER A NUMBER 
STEP 3:set sum =0 and temp equal to given number
STEP 4:while temp greater than 0 then digit is equal to temp remainder of 10
STEP 5:add sum with 3*digit and temp is divided by 10
STEP 6:if num is equal to sum then given number is an armstrong
STEP 7: DISPLAY OUTPUT
STEP 8: STOP
"""

num=int(input("Enter a number : "))
sum=0
temp=num
while(temp> 0):
    digit=temp%10
    sum+=digit**3
    temp//=10
if(num==sum):
    print(num,"is an armstrong number")
else:
    print(num,"is not an armstrong number")

