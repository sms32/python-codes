# TURTLE PROGRAM TO DESIGN A HOUSE AND A MAN

import turtle
h = turtle.Turtle()
h.width(4)
h.fillcolor("blue")
h.begin_fill()
h.forward(150)
h.left(90)
h.forward(175)
h.left(90)
h.forward(150)
h.left(90)
h.forward(175)
h.left(90)
h.forward(50)
h.left(90)
h.forward(100)
h.right(90)
h.forward(50)
h.right(90)
h.forward(100)
h.end_fill()
h.left(90)
h.forward(50)
h.left(90)
h.forward(175)
h.fillcolor("yellow")
h.begin_fill()
h.left(30)
h.forward(150)
h.left(120)
h.forward(150)
h.left(120)
h.forward(150)
h.end_fill()
h.up()
h.setposition(300,0)
h.down()
h.left(90)
h.forward(200)
h.right(90)
h.fillcolor('yellow')
h.begin_fill()
h.circle(25)
h.end_fill()
h.right(90)
h.forward(50)
h.fillcolor('blue')
h.begin_fill()
h.right(90)
h.forward(50)
h.right(90)
h.forward(25)
h.right(90)
h.forward(50)
h.right(90)
h.forward(25)
h.end_fill()
h.hideturtle()
turtle.done()
